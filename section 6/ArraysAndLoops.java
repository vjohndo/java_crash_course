public class ArraysAndLoops {
    public static void main(String[] args) {
        String[] kingdoms = {"Mercia", "Wessex", "Northumbria", "E A"};

        for (int i = 0; i < kingdoms.length; i++) {
            System.out.println("The elemant at index " + i + " is " + kingdoms[i]);
        }
    }    
}
